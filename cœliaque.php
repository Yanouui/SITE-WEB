                       <h1>
                
                        La maladie cœliaque
                       </h1> <br>
                   </div>
                  <br>
                   <div class="soustitre">
                 <h4>
                    Définition 
                 </h4>  <br>  
                   </div>  <div class="parag">
                  &nbsp;  La maladie cœliaque (MC) est un problème médical commun et touchant, selon les estimations, un pour cent de la population. Il s’agit d’un problème médical au cours duquel la surface d’absorption de l’intestin grêle est endommagée par une substance appelée gluten. Le gluten représente un groupe de protéines présentes dans le blé, le seigle et l’orge, ainsi que dans leurs céréales croisées. Les dommages infligés à l’intestin peuvent mener à divers symptômes et entraîner l’incapacité de l’organisme à absorber les nutriments comme les protéines, le gras, les glucides, les vitamines et les minéraux, essentiels à une bonne santé.
                   </div> <br>
                   <div class="soustitre1">
                  <H4> 
                    Comment se présente la maIadie coeliaque ?
                   </H4>


                   </div>  <br>
                   <div class="parag1">
                La forme du nourrisson et celle du jeune enfant est la plus classique. L’enfant présente souvent une diarrhée chronique
                ; il est fatigué, apathique et triste. Son abdomen est ballonné et ses membres fins. On note le plus souvent un
                ralentissement de la croissance en poids et en taille.
                Chez l’enfant plus âgé, la maladie peut être peu symptomatique, limitée à une petite taille, un retard d’apparition de
                la puberté, une anémie chronique, des anomalies de l’émail dentaire, des douleurs articulaires…
                Chez l’adulte, les signes de la maladie peuvent être des ballonements, des épisodes diarrhée - constipation et un
                amaigrissement inquiétant. La maladie peut être mono-symptomatique (anémie, ostéoporose…) ou atypique en se manifestant
                par des crampes musculaires, une stomatite aphteuse, des irrégularités menstruelles, des fausses couches à répétition.
                Le nombre et l’intensité des symptômes varient d’une personne à une autre.

                   </div>  <br>
                   <div class="soustitre2">
                       <H4>
                        Diagnostic
                       </H4> </div> <br>
                       <div class="parag2">
                        (1). Dépistage
                        Par le passé, les médecins devaient s’appuyer sur les symptômes et sur les signes cliniques pour suspecter un diagnostic de la MC; puis choisir quels patients devraient passer d’autres tests pour obtenir une confirmation. Maintenant, des tests sanguins très efficaces sont disponibles pour dépister la MC. L’anticorps IgA du tissu transglutaminase est le test de dépistage actuellement recommandé. Les patients doivent suivre un régime alimentaire normal (contenant du gluten) au moment du dépistage afin que le résultat soit valide.
                        <br>
                     (2). Biopsie
                        Le diagnostic formel de la MC est établi grâce à une biopsie duodénale. La biopsie est effectuée par endoscopie par un gastroentérologue. Nous vous rappelons qu’il est primordial de continuer à consommer du gluten avant la biopsie, puisque l’arrêt de la consommation pourrait compromettre le diagnostic.
